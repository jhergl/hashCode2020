package hashCode2020;

public class LibreriaSolucion {
	private int nuLibrosEnviados;
	private int indiceLibreria;
	
	private int [] libros;
	
	public int[] getLibros() {
		return libros;
	}
	public void setLibros(int[] libros) {
		this.libros = libros;
	}
	public int getNuLibrosEnviados() {
		return nuLibrosEnviados;
	}
	public void setNuLibrosEnviados(int nuLibrosEnviados) {
		this.nuLibrosEnviados = nuLibrosEnviados;
	}
	public int getIndiceLibreria() {
		return indiceLibreria;
	}
	public void setIndiceLibreria(int indiceLibreria) {
		this.indiceLibreria = indiceLibreria;
	}
}
